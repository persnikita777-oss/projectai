const slugs = Object.keys(posts)

export function generateStaticParams() {
  return slugs.map((slug) => ({ slug }))
}

export function generateMetadata({ params }: { params: { slug: string } }) {
  const post = posts[params.slug]
  if (!post) return { title: "Статья не найдена" }
  return {
    title: post.title,
    description: post.content[0],
  }
}

export default function BlogPostPage({ params }: { params: { slug: string } }) {
  const post = posts[params.slug]
  if (!post) notFound()

  const currentIndex = slugs.indexOf(params.slug)
  const prevSlug = currentIndex > 0 ? slugs[currentIndex - 1] : null
  const nextSlug = currentIndex < slugs.length - 1 ? slugs[currentIndex + 1] : null

  return (
    <div className="py-16 md:py-24">
      <div className="container mx-auto max-w-3xl px-4">
        <Link
          href="/blog"
          className="inline-flex items-center gap-1 text-sm text-muted-foreground hover:text-foreground mb-8"
        >
          <ArrowLeft className="h-4 w-4" />
          Все статьи
        </Link>

        <div className="relative h-64 md:h-80 rounded-xl overflow-hidden mb-8">
          <Image src={post.image} alt={post.title} fill className="object-cover" />
        </div>

        <div className="flex items-center gap-3 mb-4">
          <Badge>{post.category}</Badge>
          <span className="flex items-center gap-1 text-sm text-muted-foreground">
            <Clock className="h-3.5 w-3.5" />
            {post.readTime}
          </span>
          <span className="text-sm text-muted-foreground">{post.date}</span>
        </div>

        <h1 className="text-3xl md:text-4xl font-bold mb-8">{post.title}</h1>

        <div className="prose prose-neutral dark:prose-invert max-w-none">
          {post.content.map((block, i) => {
            if (block.startsWith("## ")) {
              return (
                <h2 key={i} className="text-xl font-bold mt-8 mb-3">
                  {block.replace("## ", "")}
                </h2>
              )
            }
            return (
              <p key={i} className="text-muted-foreground leading-relaxed mb-4">
                {block}
              </p>
            )
          })}
        </div>

        {/* CTA */}
        <Card className="mt-12">
          <CardContent className="pt-6 text-center">
            <h3 className="text-lg font-bold mb-2">Хотите внедрить AI в свой бизнес?</h3>
            <p className="text-muted-foreground mb-4">
              Расскажите о задаче — мы подготовим решение за 30 минут
            </p>
            <Button asChild>
              <Link href="/brief">
                Оставить заявку
                <ArrowRight className="ml-2 h-4 w-4" />
              </Link>
            </Button>
          </CardContent>
        </Card>

        {/* Navigation */}
        <div className="flex justify-between mt-8">
          {prevSlug ? (
            <Link
              href={`/blog/${prevSlug}`}
              className="flex items-center gap-1 text-sm text-muted-foreground hover:text-foreground"
            >
              <ArrowLeft className="h-4 w-4" />
              Предыдущая
            </Link>
          ) : <div />}
          {nextSlug ? (
            <Link
              href={`/blog/${nextSlug}`}
              className="flex items-center gap-1 text-sm text-muted-foreground hover:text-foreground"
            >
              Следующая
              <ArrowRight className="h-4 w-4" />
            </Link>
          ) : <div />}
        </div>
      </div>
    </div>
  )
}
